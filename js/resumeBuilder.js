var replaceData = function(formatter, content){
	return formatter.replace("%data%", content);
};

var AppendObjectPropertiesAndValues = function(object, location, template,PropertyPlaceHolder, ValuePlaceHolder){
	for (var property in object){
		var value = object[property];
		var temp = template.replace(PropertyPlaceHolder, property)
						   .replace(ValuePlaceHolder, value);
		$(location).append(temp);
	}
};

var AppendArrayValues = function(array, location, template, ValuePlaceHolder){
	for (var i = 0; i < array.length; ++i){
		var data = template.replace(ValuePlaceHolder, array[i]);
		$(location).append(data);
	}
};

